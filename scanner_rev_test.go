func TestRevWhileRune(t *testing.T) {
	cases := []struct {
		inp string
		r   rune
		exp headTail
	}{
		{"123-", '-', headTail{"123", "-"}},
		{"a....", '.', headTail{"a", "...."}},
		{"   ", ' ', headTail{"", "   "}},
	}

	for i, c := range cases {
		sca := NewScanner(c.inp)
		sca.ToEnd()
		sca.RevWhileRune(c.r)
		if e := c.exp.check(i, sca); e != nil {
			t.Errorf("%v", e)
		}
	}
}

